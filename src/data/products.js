export function getProductById(productId) {
  return products.find((product) => product.id === parseInt(productId))
}

export function getProductBySlug(slug) {
  return products.find((product) => product.slug === slug)
}

export function getProductsByCategory(categoryId, subcategoryId = null) {
  if (subcategoryId) {
    return products.filter(
      (product) => product.categoryId === categoryId && product.subcategoryId === subcategoryId,
    )
  }
  return products.filter((product) => product.categoryId === categoryId)
}

export function getNewProducts(limit = 4) {
  return products.filter((product) => product.new).slice(0, limit)
}

export function getBestsellers(limit = 4) {
  return products.filter((product) => product.bestseller).slice(0, limit)
}

export function getRandomProducts(limit = 4, excludedProductId = null) {
  const filteredProducts = excludedProductId
    ? products.filter((product) => product.id !== excludedProductId)
    : products
  const shuffled = [...filteredProducts].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, limit)
}
